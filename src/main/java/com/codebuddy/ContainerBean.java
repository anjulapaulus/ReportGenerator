package com.codebuddy;

public class ContainerBean {
    private String containerNum;
    private String sealNum;
    private String size;
    private String status;
    private String packages;
    private String containerCBM;

    public String getContainerNum() {
        return containerNum;
    }

    public void setContainerNum(String containerNum) {
        this.containerNum = containerNum;
    }

    public String getSealNum() {
        return sealNum;
    }

    public void setSealNum(String sealNum) {
        this.sealNum = sealNum;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPackages() {
        return packages;
    }

    public void setPackages(String packages) {
        this.packages = packages;
    }

    public String getContainerCBM() {
        return containerCBM;
    }

    public void setContainerCBM(String containerCBM) {
        this.containerCBM = containerCBM;
    }
}
